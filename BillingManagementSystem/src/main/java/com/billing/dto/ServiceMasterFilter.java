package com.billing.dto;

public class ServiceMasterFilter {
	private String serviceID;
	private String serviceName;
	private String serviceIDName;
	private String organizationID;
	private String organizationName;
	private String organizationIDName;
	
	
	public String getServiceID() {
		return serviceID;
	}
	public void setServiceID(String serviceID) {
		this.serviceID = serviceID;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getServiceIDName() {
		return serviceIDName;
	}
	public void setServiceIDName(String serviceIDName) {
		this.serviceIDName = serviceIDName;
	}
	public String getOrganizationID() {
		return organizationID;
	}
	public void setOrganizationID(String organizationID) {
		this.organizationID = organizationID;
	}
	public String getOrganizationName() {
		return organizationName;
	}
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}
	public String getOrganizationIDName() {
		return organizationIDName;
	}
	public void setOrganizationIDName(String organizationIDName) {
		this.organizationIDName = organizationIDName;
	}
	
	
}
